// The radius of a circle is 30 meters
// # calculate the area of a circle with radius of 30 meters
radius = 30
area_of_circle = math.pi * radius ** 2
print("Area of circle with radius 30 meters:", area_of_circle)

// # calculate the circumference of a circle with radius of 30 meters
circum_of_circle = 2 * math.pi * radius
print("Circumference of circle with radius 30 meters:", circum_of_circle)

// # calculate the area of a circle with user input radius
user_radius = float(input("Enter the radius of the circle: "))
area_of_circle = math.pi * user_radius ** 2
print("Area of circle with radius", user_radius, "meters:", area_of_circle)

